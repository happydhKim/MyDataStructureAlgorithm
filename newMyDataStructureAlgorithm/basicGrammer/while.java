// 자꾸 헷갈려 매번!!!!!!!!!!!!!!!!!!!!

public class while {
	public static void main(String args[]) {
		boolean act = false;
		while(!act) { // act 가 false 이면 계속 돌아요!!
			for (int i = 0; i < 10; i++) {
				System.out.println(i);

			}
			act = true;
		}

    int a = 2;
		while(a>0) { // a 가 0보다 크면 계속 돌아요!!!
			for (int i = 0; i < 10; i++) {
				System.out.println(i);

			}
			a--;
		}


	}
}
